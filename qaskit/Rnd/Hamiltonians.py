def Standard_to_Pennylane_Observables(chem_obj_std, bond_len):
    import pennylane as qml
    pennylane_obj = {
        'fun': [],
        'obs': [],
        'coe': chem_obj_std[str(bond_len)]['Hamiltonian']['coe'].copy(),
        'const': chem_obj_std[str(bond_len)]['Hamiltonian']['const'],
        'qubit': chem_obj_std[str(bond_len)]['Hamiltonian']['qubit']
    }

    for pauli_string in chem_obj_std[str(bond_len)]['Hamiltonian']['obs']:
        penny_pauli_string = None
        if pauli_string[0] == 'I':
            penny_pauli_string = qml.Identity(0)
        elif pauli_string[0] == 'X':
            penny_pauli_string = qml.PauliX(0)
        elif pauli_string[0] == 'Y':
            penny_pauli_string = qml.PauliY(0)
        elif pauli_string[0] == 'Z':
            penny_pauli_string = qml.PauliZ(0)

        for i in range(1, chem_obj_std[str(bond_len)]['Hamiltonian']['qubit']):
            if pauli_string[i] == 'I':
                penny_pauli_string = penny_pauli_string @ qml.Identity(i)
            elif pauli_string[i] == 'X':
                penny_pauli_string = penny_pauli_string @ qml.PauliX(i)
            elif pauli_string[i] == 'Y':
                penny_pauli_string = penny_pauli_string @ qml.PauliY(i)
            elif pauli_string[i] == 'Z':
                penny_pauli_string = penny_pauli_string @ qml.PauliZ(i)
        pennylane_obj['obs'].append(penny_pauli_string)
        pennylane_obj['fun'].append(linear_func)
    return pennylane_obj


def Pennylane_Observable_FCI_Calculation(penny_observable):
    import numpy as np
    matrix = penny_observable['const'] * np.identity(2 ** penny_observable['qubit'], complex)
    for i in range(len(penny_observable['obs'])):
        matrix += penny_observable['coe'][i] * penny_observable['obs'][i].matrix
    # print(matrix)
    import numpy

    l, v = numpy.linalg.eig(matrix)
    l.sort()
    return np.real(l[0])


